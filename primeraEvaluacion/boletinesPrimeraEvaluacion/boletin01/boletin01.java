package boletin01;

/**
 * Clase para calcular el area de un triángulo
 *
 * @author angel
 */
public class boletin01 {

    /**
     * Declaramos variables de base y altura para el cálculo del área.
     *
     * @param args the command line arguments
     */
    public static void main(String[] args) {

        double base = 4;
        double altura = 3;
        double area = base * altura / 2;

        System.out.println(area);
    }

}
